          <script type="text/javascript">
            var hidePanelIndex = 0,
                set = 0;
            function show_h3() {
                var $this = $(this).siblings();
                $('.tab-header-brother').not($this).hide(600);
                if ($this.is(':visible')) {
                    $this.show(600);
                } else {
                    hidePanelIndex = $this.attr('data-rel');
                    $this.show(600);
                }
            }
            function clickClass($this) {
                var url = $this.attr('href');
                $('.tab-header-brother').show();
                $('.hidePanel').hide();
                $('#anchor_tabs_container li a').removeClass('tab-active');
                $this.addClass('tab-active').blur();
                hidePanelIndex = $(url).find('.tab-header-brother').attr('data-rel');
                $(url).fadeIn(250);
            }
            function adjustStyle(width) {
                width = parseInt(width);
                if (jQuery.browser.mobile || width <= 641) {
                    $('#anchor_tabs_container').hide();

                    $('.hidePanel').show().children().not('h3').hide()

                    $('.header').addClass('mobile-click');

                    $('h3.mobile-click').eq(hidePanelIndex).click();
                } else {
                    $('#anchor_tabs_container').show();

                    $('.hidePanel').hide()

                    $('.header').removeClass('mobile-click');

                    $('#anchor_tabs_container li:eq(' + hidePanelIndex + ') a').click();
                }
            }
            $(document)
            .on('pageinit', function () {
                adjustStyle($(this).width());
            })
            .on('click', 'h3.mobile-click', show_h3)
            .on('click', '#anchor_tabs_container li a', function (e) {
                e.preventDefault();
                clickClass($(this));
            });
            $(window).on('resize', function (e) {
                var counter = (++set);
                setTimeout(function () {
                    if (counter === set) {
                        adjustStyle($(window).width());
                    }
                }, 500);
            });
        </script>

